package com.errday.headfirstdesign.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("turn on Popcorn Popper");
    }

    public void pop() {
        System.out.println("make popcorn");
    }

    public void off() {
        System.out.println("turn off Popcorn Popper");
    }
}
